package com.deepak.android.blogboard;

/**
 * model class to retrieve user data.
 */
public class User {
    public String image;
    public String name ;

    public User(){}

    public User(String image, String name) {
        this.image = image;
        this.name = name;
    }

    public String getImage() {
        return image;
    }

    public String getName() {
        return name;
    }








}

